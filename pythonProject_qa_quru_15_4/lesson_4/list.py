# ДЕАЛЕМ СПИСКИ

l = [1, 2, 3, 'a', 'b', 'c', [4, 5, 6]] # список в квадратных скобках, втрнуи которого может быть ещё один список

print(l[0]) # показать первый элемент в спимске
print(l[-1]) # показать последний элемент в списке

print(l[-1][0]) # показать первый элемент в в списке, сзади

print(l[::-1]) # проидексировали список наоборот

# ФУНКЦИИ СПИСКОВ

l.append("new element") # добавить новый элемент в список
l.extend(['new', 'list']) # добавить новый список c элементами
len(l) # Посмотреть сколько элементов в списке
l.reverse() # Развернуть элемент, как индексация на оборот

l[0] = 200 # заменить элемент на указанное знаение

print(l)

