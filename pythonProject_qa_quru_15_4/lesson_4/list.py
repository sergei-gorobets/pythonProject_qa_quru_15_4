# МНОЖЕСТВА
# (пишутся в фигурных скобках, обязательно содержат в себе только уникальные значения и не содержат сортировку)
# специальный тип данных, который предназначен, чтобы работать с данными как с множеством

s1 = {1,2, 3, 4, 5, 5, 5, 5, 5}
s2= {1,2, 5, 5}

print(s1)
print(s2)

s1.intersection(s2) # посмотреть пересечения двух миножеств
s1 - s2 # вычесть из одного множества другое и посмотреть что останется

print(list(set([1,2 ,3, 4, 5, 5, 5]))) # Здесь есть список [1,2 ,3, 4, 5, 5], командой set мы приводим его в множество,
